package NemoProject;

public class Coordenate {
    public static int x = 0;
    public static int y = 0;

    public static void incrementX() {
        x++;
    }

    public static void decrementX() {
        x--;
    }

    public static void incrementY() {
        y++;
    }

    public static void decrementY() {
        y--;
    }
}